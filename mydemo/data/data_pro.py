import pandas as pd

df = pd.read_csv('data.csv', names=[
    '唯一标识', '所在区域', '景点名称', '评论数量', '评分',
    '是否为广告内容', '是否被推荐', '城市名称', '图片',
    '与市中心的距离', '标签', '详情页链接', '市场票价',
    '优惠价格', '优惠描述', '实际票价', '价格类型',
    '价格类型描述', '是否免费', '经度', '纬度', '热度评分', '省份'
])

# 根据'唯一标识'列去重
df = df.drop_duplicates(subset=['唯一标识'])
df['评分'] = df['评分'].fillna(0)

# 清理标签列中的特殊字符（方括号和引号）
df['标签'] = df['标签'].str.replace('[', '').str.replace(']', '').str.replace('"', '')

# 填充热度评分列的缺失值为0
df['热度评分'] = df['热度评分'].fillna(0)

# 填充评论数量列的缺失值为0
df['评论数量'] = df['评论数量'].fillna(0)

# 将清洗后的数据保存为CSV文件，不保留索引
df.to_csv('data_pro.csv', index=False)